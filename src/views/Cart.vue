							</p>
						</div>
					</section>
				</section>
				<!-- 優惠和小計區塊 -->
				<aside class="md:w-1/3 flex flex-col gap-5 mt-5 md:mt-0">
					<!-- 優惠區塊 -->
					<div class="sticky top-0">
