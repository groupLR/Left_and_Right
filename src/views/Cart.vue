							<div v-if="error" class="text-red-500 text-center">{{ error }}</div>

							<!-- 無可用優惠券 -->

							<div v-if="validCoupon" class="flex items-start flex-col">
								<p class="my-4 px-5 bg-green-100 text-center text-sm md:text-base">優惠促銷</p>
